package beans;

public class Cadastro {
	private String Colaborador, Cargo, SBruto, ValeTransporte, ImpoRenda, FaltasM, SLiquido;

	public String getColaborador() {
		return Colaborador;
	}

	public void setColaborador(String colaborador) {
		Colaborador = colaborador;
	}

	public String getCargo() {
		return Cargo;
	}

	public void setCargo(String cargo) {
		Cargo = cargo;
	}

	public String getSBruto() {
		return SBruto;
	}

	public void setSBruto(String sBruto) {
		SBruto = sBruto;
	}

	public String getValeTransporte() {
		return ValeTransporte;
	}

	public void setValeTransporte(String valeTransporte) {
		ValeTransporte = valeTransporte;
	}

	public String getImpoRenda() {
		return ImpoRenda;
	}

	public void setImpoRenda(String impoRenda) {
		ImpoRenda = impoRenda;
	}

	public String getFaltasM() {
		return FaltasM;
	}

	public void setFaltasM(String faltasM) {
		FaltasM = faltasM;
	}

	public String getSLiquido() {
		return SLiquido;
	}

	public void setSLiquido(String sLiquido) {
		SLiquido = sLiquido;
	}
	

}
